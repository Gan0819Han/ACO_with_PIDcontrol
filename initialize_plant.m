function [plant, Kp_zn, Ki_zn, Kd_zn] = initialize_plant()
% =========================================================================
%                         初始化被控对象和初始PID参数
% =========================================================================
%
% 功能:
% 1. 定义被控对象的传递函数。
% 2. 提供一组用于初始化搜索范围的PID参考参数。
%
% 修改建议:
% - 您可以将下面的 'plant' 替换为您自己项目的传递函数模型。
% - 'Kp_zn', 'Ki_zn', 'Kd_zn' 是一组粗略的初始值，用于定义搜索范围，
%   不要求非常精确，但应在合理的数量级。
%
% =========================================================================

    % --- 1. 定义被控对象的传递函数 ---
    % 例如，一个典型的二阶系统: G(s) = 1 / (s^2 + 10s + 20)
    s = tf('s');
    plant = 1 / (s^2 + 10*s + 20);

    % --- 2. 提供一组初始PID参数 (例如通过Z-N方法或其他方法估算) ---
    % 这些值仅用于设定ACO算法的初始搜索空间，并非直接使用。
    Kp_zn = 30;
    Ki_zn = 70;
    Kd_zn = 10;
end
